// switch statement
 const months = 3

 switch (months) {
    case 1:
        console.log("january");
        
        break;
    case 2:
        console.log("February");
        
        break;
    case 3:
        console.log("March");
        
        break;
    case 4:
        console.log("April");
        
        break;

 
    default:
        console.log("Month not found");
        
        break;
 }